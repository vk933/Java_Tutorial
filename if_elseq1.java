public class if_elseq1 {
    public static void main(String[] args) {
        String city = "siwan";
        if(city == "siwan") {
            System.out.println("city is siwan");
        }else if (city == "Noida") {
            System.out.println("city is noida");
        }else if(city == "patna") {
            System.out.println("city is patna");
        }else {
            System.out.println(city);
        }
    }
}
